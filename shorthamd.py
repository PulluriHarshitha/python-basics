#syntax : value_if_true if condition else value_if_false

#simple example
a = 50
b = 30
print("a is greater") if a > b else print("b is greater")

#assigning a value
age = 18
status = "Adult" if age >= 18 else "Minor"
print(status)

#even or odd
num = 6
result = "Even" if num % 2 == 0 else "Odd"
print(result)
