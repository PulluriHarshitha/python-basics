 #ABSTRACT() function
# An integer
var = 3150
print('Absolute value of integer is:', abs(var))

#floating point number
float_number = -1234
print('Absolute value of float is:',abs(float_number))

#abs() a complex number

complex_number = (3 - 5j)
print('Absolute value or Magnitude of complex is:', abs(complex_number))